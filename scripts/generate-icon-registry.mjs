import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "fs";
import { join, resolve } from "path";

const projectRoot = resolve(import.meta.dirname, "..");
const assetsDir = join(projectRoot, "src", "assets");
const sharedTypesPath = join(projectRoot, "src", "types", "shared.d.ts");
const registryDir = join(projectRoot, "src", "registry");

const variantWordToNumberMap = {
  one: "1",
  two: "2",
  three: "3",
  four: "4",
};

const usageNameOverrides = {
  PlusIcon: "plus",
};

const convertExportNameToUsageName = (exportName) => {
  if (usageNameOverrides[exportName]) {
    return usageNameOverrides[exportName];
  }

  const exportNameWithoutSuffix = exportName.replace(/Icon$/, "");
  const kebabCaseName = exportNameWithoutSuffix
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .toLowerCase();

  return kebabCaseName.replace(
    /variant-(one|two|three|four)/g,
    (_, variantWord) => `variant-${variantWordToNumberMap[variantWord]}`
  );
};

const readIconKeysFromSharedTypes = () => {
  const sharedTypesFile = readFileSync(sharedTypesPath, "utf-8");
  return Array.from(
    sharedTypesFile.matchAll(/\|\s*"([^"]+)"/g),
    (match) => match[1]
  );
};

const readComponentExportName = (folderName) => {
  const indexFilePath = join(assetsDir, folderName, "index.ts");
  const indexFileContents = readFileSync(indexFilePath, "utf-8");
  const namedExportMatch = indexFileContents.match(
    /export\s+\{\s*(\w+)\s*\}/
  );

  if (namedExportMatch) {
    return namedExportMatch[1];
  }

  const namedImportMatch = indexFileContents.match(
    /import\s+\{\s*(\w+)\s*\}\s+from/
  );

  if (namedImportMatch) {
    return namedImportMatch[1];
  }

  const defaultImportMatch = indexFileContents.match(/import\s+(\w+)\s+from/);
  return defaultImportMatch ? defaultImportMatch[1] : folderName;
};

const buildIconEntries = () => {
  const assetFolders = readdirSync(assetsDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((firstFolder, secondFolder) =>
      firstFolder.localeCompare(secondFolder)
    );

  return assetFolders
    .filter((folderName) => {
      const indexFilePath = join(assetsDir, folderName, "index.ts");
      const iconFilePath = join(assetsDir, folderName, `${folderName}.tsx`);
      return existsSync(indexFilePath) && existsSync(iconFilePath);
    })
    .map((folderName) => {
      const componentExportName = readComponentExportName(folderName);
      const usageName = convertExportNameToUsageName(componentExportName);

      return {
        folderName,
        componentExportName,
        usageName,
      };
    });
};

const validateRegistry = (iconEntries, iconKeys) => {
  const usageNamesFromAssets = new Set(
    iconEntries.map((entry) => entry.usageName)
  );
  const missingKeys = iconKeys.filter((key) => !usageNamesFromAssets.has(key));
  const extraEntries = iconEntries.filter(
    (entry) => !iconKeys.includes(entry.usageName)
  );

  if (missingKeys.length > 0 || extraEntries.length > 0) {
    const errorMessages = [];

    if (missingKeys.length > 0) {
      errorMessages.push(
        `Missing asset mappings for icon keys: ${missingKeys.join(", ")}`
      );
    }

    if (extraEntries.length > 0) {
      errorMessages.push(
        `Unmapped asset folders: ${extraEntries
          .map((entry) => entry.folderName)
          .join(", ")}`
      );
    }

    throw new Error(errorMessages.join("\n"));
  }
};

const generateIconExportsFile = (iconEntries) => {
  const importLines = iconEntries
    .map(
      (entry) =>
        `import ${entry.componentExportName} from "../assets/${entry.folderName}";`
    )
    .join("\n");

  const exportLines = iconEntries
    .map((entry) => `  ${entry.componentExportName},`)
    .join("\n");

  return `${importLines}

export {
${exportLines}
};
`;
};

const generateIconRegistryFile = (iconEntries) => {
  const importLines = iconEntries
    .map(
      (entry) =>
        `import ${entry.componentExportName} from "../assets/${entry.folderName}";`
    )
    .join("\n");

  const registryLines = iconEntries
    .map((entry) => `  "${entry.usageName}": ${entry.componentExportName},`)
    .join("\n");

  return `import type { ComponentType } from "react";

${importLines}

import type { IconProps, Icons } from "../types/shared";

const iconRegistry: Record<Icons, ComponentType<IconProps>> = {
${registryLines}
};

export { iconRegistry };
`;
};

const generateIndexFile = () => {
  return `export { default, PrettyIcons } from "./components/PrettyIcons";
export * from "./registry/iconExports";
`;
};

const writeGeneratedFile = (filePath, contents) => {
  const banner = "// This file is auto-generated by scripts/generate-icon-registry.mjs\n";
  writeFileSync(filePath, `${banner}${contents}`, "utf-8");
};

const main = () => {
  const iconKeys = readIconKeysFromSharedTypes();
  const iconEntries = buildIconEntries();

  validateRegistry(iconEntries, iconKeys);

  const sortedEntries = [...iconEntries].sort((firstEntry, secondEntry) =>
    firstEntry.usageName.localeCompare(secondEntry.usageName)
  );

  mkdirSync(registryDir, { recursive: true });

  writeGeneratedFile(
    join(registryDir, "iconExports.ts"),
    generateIconExportsFile(sortedEntries)
  );
  writeGeneratedFile(
    join(registryDir, "iconRegistry.ts"),
    generateIconRegistryFile(sortedEntries)
  );
  writeGeneratedFile(join(projectRoot, "src", "index.ts"), generateIndexFile());

  console.log(`Generated registry for ${sortedEntries.length} icons.`);
};

main();
